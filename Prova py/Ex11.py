N = int(input("Digite um número correspondente ao dia da semana; exemplo: 1 = Domingo: "))

if (N == 1):
    print("Domingo")
elif (N == 2):
    print("Segunda")
elif (N == 3):
    print("Terça")
elif (N == 4):
    print("Quarta")
elif (N == 5):
    print("Quinta")
elif (N == 6):
    print("Sexta")
elif (N == 7):
    print("Sábado")